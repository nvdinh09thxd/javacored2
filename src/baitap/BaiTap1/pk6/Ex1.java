package baitap.BaiTap1.pk6;

public class Ex1 {
	public static void main(String[] args) {
		// Dùng vòng lặp for in ra các số từ 1 đến 10
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();

		// Dùng vòng lặp for in ra các số từ 10 đến 1
		for (int i = 10; i >= 1; i--) {
			System.out.print(i + "\t");
		}
	}
}
